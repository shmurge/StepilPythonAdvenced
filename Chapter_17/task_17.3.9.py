# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –º–µ–Ω–µ–¥–∂–µ—Ä –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ üôÇ.

with open('lines.txt') as file:
    context = [i.strip('\n') for i in file.readlines()]
    max_len = max(len(word) for word in context)
    new_context = [i for i in context if len(i) == max_len]
    print(*new_context, sep='\n')

