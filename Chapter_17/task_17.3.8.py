# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 4. –ù–µ –∑–∞–±—ã–≤–∞–π—Ç–µ –ø—Ä–æ —Å–∏–º–≤–æ–ª –∫–æ–Ω—Ü–∞ —Å—Ç—Ä–æ–∫–∏ '\n'.

with open('data.txt') as file:
    context = [i.strip('\n') for i in file.readlines()]
    context = context[::-1]
    for line in context:
        print(line, sep='\n')
