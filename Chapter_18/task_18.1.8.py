# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –Ω–∞–∑–≤–∞–Ω–∏—è –≤—Å–µ—Ö —Ñ—É–Ω–∫—Ü–∏–π (–Ω–µ –º–µ–Ω—è—è –ø–æ—Ä—è–¥–∫–∞ –∏—Ö —Å–ª–µ–¥–æ–≤–∞–Ω–∏—è –≤ –∏—Å—Ö–æ–¥–Ω–æ–º —Ñ–∞–π–ª–µ),
# –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ, –¥–ª—è –∫–æ—Ç–æ—Ä—ã—Ö –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –ø–æ—è—Å–Ω—è—é—â–∏–π –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π. –ï—Å–ª–∏ –≤—Å–µ —Ñ—É–Ω–∫—Ü–∏–∏ –≤ —Ñ–∞–π–ª–µ –∏–º–µ—é—Ç
# –ø–æ—è—Å–Ω—è—é—â–∏–π –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π, —Ç–æ —Å–ª–µ–¥—É–µ—Ç –≤—ã–≤–µ—Å—Ç–∏: Best Programming Team.

original_ls = []
result_ls = []
with open('code.txt') as input_file:
    for line in input_file:
        s = line.strip()
        original_ls.append(s)

for i in range(len(original_ls)):
    if original_ls[i - 1] != '' and original_ls[i - 1][0] == '#':
        continue
    ls = original_ls[i].split()
    for j in range(len(ls)):
        if ls[j] == 'def':
            s = ls[j + 1]
            index = s.index('(')
            result_ls.append(s[:index])

if len(result_ls) == 0:
    result_ls.append('Best Programming Team')

print(*result_ls, sep='\n')
